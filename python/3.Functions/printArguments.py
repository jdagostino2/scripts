# Print on different lines
# Hello
# World
print('Hello')
print('World')

# Print on the same line
# Hello World
print('Hello ', end='')
print('World')

# Print multiple outputs same line
# cats dogs mice
print('cats', 'dogs', 'mice')

# Print multiple with a defined sep
# cats;dogs;mice
print('cats', 'dogs', 'mice', sep=';')
